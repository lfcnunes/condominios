    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            controller.destroy(intID);
            btnCloseActionPerformed(evt);
            
        } catch (NonexistentEntityException ex) {
            Logger.getLogger(frameApartamentoForm.class.getName()).log(Level.SEVERE, null, ex);
            
            JOptionPane.showMessageDialog(null, "Erro ao apagar o registo.", "Apagar", JOptionPane.ERROR_MESSAGE);
        }             
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
       if (proprietario == null) {
           JOptionPane.showMessageDialog(null, "Ocorreu um erro o proprietário não foi encontrado.", "Erro", JOptionPane.ERROR_MESSAGE);
       }
       try {
            proprietario.setNome(txtNome.getText());
            proprietario.setTelefone(txtTelefone.getText());
           
            if (intID == 0) {
                controller.create(proprietario);                           
            } else {
                controller.edit(proprietario);
            }
            
            btnCloseActionPerformed(evt);            
       
        } catch (Exception ex) {
            Logger.getLogger(frameProprietarioForm.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Ocorreu um erro gravar o proprietário.", "Erro", JOptionPane.ERROR_MESSAGE);
        }      
    }//GEN-LAST:event_btnOKActionPerformed

