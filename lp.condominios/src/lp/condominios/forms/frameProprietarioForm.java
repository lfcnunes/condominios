    public frameProprietarioForm(int ID) {
        this();
        
        intID = ID;
        
          // Conectar o apartamento
        controller = new ProprietarioJpaController(entityManager1.getEntityManagerFactory());
  
        if (intID == 0) {
            this.setTitle("Adicionar Proprietário");
            btnDelete.setVisible(false);
            
            proprietario = new Proprietario();
        } else {
            this.setTitle("Editar Proprietário: "  + intID);
            
            // Ler os dados da base de dados
            proprietario = controller.findProprietario(intID);
           
            txtID.setText(proprietario.getId().toString());
            txtNome.setText(proprietario.getNome().toString());
            txtTelefone.setText(proprietario.getTelefone().toString());
        }
    }
    
