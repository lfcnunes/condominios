    private void SetTableModel(List<Proprietario> lst) {
        DefaultTableModel model = new DefaultTableModel(new String[]{"ID", "Nome", "Telefone"}, 0);
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy HH:mm");   
        
        for (int i = 0; i < lst.size(); i++) {
            List<String> row = new ArrayList<>();
            row.add(lst.get(i).getId().toString());
            row.add(lst.get(i).getNome());
            row.add(lst.get(i).getTelefone());

            model.addRow(row.toArray());
        }

        jTable1.setModel(model);
    }
    
