    public frameMovimentoForm(int ID) {
        this();
        
        this.intID = ID;
        
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy HH:mm");
        
        Date d = new Date();
        txtMes.setText(new Integer(d.getMonth()).toString());
        txtAno.setText(new Integer((1900 + d.getYear())).toString());
        
        // Conectar o apartamento
        controller = new MovimentoJpaController(entityManager1.getEntityManagerFactory());
        
        cmbApartamento.setModel(new ApartamentoModelList(entityManager1.getEntityManagerFactory()));
        
        if (intID == 0) {
            this.setTitle("Adicionar Movimento");
            btnDelete.setVisible(false);
            
            txtValor.setText("0,00");
            txtData.setText(dateFormat.format(new Date()));
            
            movimento = new Movimento();
            
        } else {
            this.setTitle("Editar Movimento: "  + intID);
            
            // Ler os dados da base de dados
            movimento = controller.findMovimento(intID);
            
            txtID.setText(movimento.getId().toString());
            txtDescricao.setText(movimento.getDescricao().toString());
            cmbApartamento.setSelectedIndex(movimento.getIdapartamento());
            txtValor.setText(movimento.getValor().toString());
            txtData.setText(dateFormat.format(movimento.getData()));
            txtMes.setText(new Integer(movimento.getMes()).toString());
            txtAno.setText(new Integer(movimento.getAno()).toString());
        }
    }
    
