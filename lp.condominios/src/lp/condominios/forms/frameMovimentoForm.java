    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        if (movimento == null) {
           JOptionPane.showMessageDialog(null, "Ocorreu um erro o movimento não foi encontrado.", "Erro", JOptionPane.ERROR_MESSAGE);
       }
       
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy HH:mm");  
        
        
        BigDecimal d = new BigDecimal(txtValor.getText());
        try {
            d = calcularValor(d.doubleValue(), 
                    Integer.parseInt(txtMes.getText()), 
                    Integer.parseInt(txtAno.getText()), dateFormat.parse(txtData.getText()));
            
        } catch (ParseException ex) {
            Logger.getLogger(frameMovimentoForm.class.getName()).log(Level.SEVERE, null, ex);
            
        }
               
        try {
            movimento.setDescricao(txtDescricao.getText());
            movimento.setIdapartamento(((Apartamento)cmbApartamento.getSelectedItem()).getId());
            movimento.setValor(d);
            movimento.setData(dateFormat.parse(txtData.getText()));
            movimento.setMes(Integer.parseInt(txtMes.getText()));
            movimento.setAno(Integer.parseInt(txtAno.getText()));
            
            if (intID == 0) {
                controller.create(movimento);                           
            } else {
                controller.edit(movimento);
            }
            
            btnCloseActionPerformed(evt);            
       
        } catch (Exception ex) {
            Logger.getLogger(frameProprietarioForm.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Ocorreu um erro gravar o movimento.", "Erro", JOptionPane.ERROR_MESSAGE);
        }  
    }//GEN-LAST:event_btnOKActionPerformed

