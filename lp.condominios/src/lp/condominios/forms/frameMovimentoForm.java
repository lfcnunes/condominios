    private BigDecimal calcularValor(double d, int Mes, int Ano, Date data) { 
        if (data.getDate() <= 8 && (data.getMonth() + 1) > Mes && (1900 + data.getYear()) >= Ano) {
           d = d + (d * 0.02);
        } else {
            if ((data.getMonth() + 1) > Mes && (1900 + data.getYear()) >= Ano) {
                 d = d + (d * 0.05);
            }            
        }
        
        return new BigDecimal(d);       
    }
    
